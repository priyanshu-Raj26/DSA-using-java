// Add 1 to an integer using Bit Manipulation?

public class Problem_03 {
    
    public static void main(String[] args) {
        int n = 10;
        System.out.println(n + " + " + 1 + " is: " + -~n );
        
        n = 15;
        System.out.println(n + " + " + 1 + " is: " + -~n );
        
        n = 5;
        System.out.println(n + " + " + 1 + " is: " + -~n );
        
    }
}
